'use strict';

function sum(a, b) {
    return a + b;
}

console.log(sum(4, 9));

function subtraction(a, b) {
    return a - b;
}

console.log(subtraction(23, 5));

function multiplication(a, b) {
    return a * b;
}


console.log(multiplication(12, 8));

function division(a, b) {
    return a / b;
}

console.log(division(25, 4));
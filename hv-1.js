/*
1. Число 10 складываем с числом 10, получаем 20
2. 20 складываем со строчным элементом 10, поэтому происходит приведение типов, конкатенацией получаем 2010
3. Полученное число 2010 присваиваем в переменную result
4. С помощью console.log выводим переменную result в консоль
*/
let result = 10 + 10 + "10";
console.log(result);

/*
1. Складываем число 10 со строчным элементом 10, поэтому происходит приведение типов, конкатенацией получаем 1010
2. Складываем строчный элемент 1010 с числом 10, поэтому происходит приведение типов, конкатенацией получаем 101010
3. Полученное число 101010 присваиваем в переменную result1
4. С помощью console.log выводим переменную result1 в консоль
*/

console.log(result1);

/*
1. Складываем число 10 и 10, получаем 20
2. Складываем число 20 со строчным элементом 10, у которого стоит унарный плюс, превращающий его в число, получаем 30
3. Полученное число 30 присваиваем в переменную result2
4. С помощью console.log выводим переменную result2 в консоль
*/
let result2 = 10 + 10 + +"10";
